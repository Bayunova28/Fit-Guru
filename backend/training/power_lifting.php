          <p>At some point, you’ve probably seen some dude come into the gym—he probably had a beard—load up a barbell with so many 45-lb. plates that the steel was literally bending, and send shockwaves of awesomeness across the floor with some of the most impressive squats you’ve ever witnessed in person. And at that moment, you probably wondered: How in the hell do I do that?</p>
          <p>Unlike bodybuilding, which emphasizes the pursuit of a particular physique, powerlifting is a sport of attaining as much raw strength as is humanly possible. Primal in its simplicity, powerlifting ain’t always pretty, nor is it especially glamorous. But it is practical, unquestionably hardcore, and a virtual guarantee you will never again struggle to move your couch.</p>
          <p>“To be a powerlifter means being dedicated to a goal: Find out how strong you can get, and then get stronger than that,” says Sean Collins, C.S.C.S., a certified USA Powerlifting Club Coach and owner/coach at Murder of Crows Barbell Club in Brooklyn, New York. “A powerlifter takes three seemingly simple exercises—the deadlift, squat, and bench press—and works to learn and excel in every single aspect of them, since strength is just as much a skill as it is a quantifiable characteristic.”</p>
          <p class="bold">What Does It Mean to Be a Powerlifter</p>
          <p>On its face, powerlifting is pretty simple: Develop as much all-out strength as you humanly can, primarily through the “big three” fundamental lifts: low-bar back squat, bench press, and deadlift. But talk to powerlifters long enough, and you’ll discover that they often develop a mystic sense about their training. Like marathon runners and Ironman triathletes, powerlifters operate at the brutal edge of their own physical capability.</p>
          <p>That means powerlifting is demanding and extremely taxing, both physically and mentally. There’s no faking it: Just you and a barbell, on a platform, locked in an eternal battle against gravity. And for lifters who want to want to go beyond merely “exercising” and dedicate themselves to the pursuit of hardcore strength, it’s rewarding—and, ultimately, transformative.</p>
          <p>And while powerlifting has a somewhat brutish reputation—powerlifters like to say their sport is just picking things up and putting them down—anyone who’s bench-pressed twice their body weight or deadlifted 500 lbs. knows that powerlifting is both a sport of mental fortitude and finesse.</p>
          <p>“There’s so much to learn: bracing, rooting, breathing, optimal foot position, bar position, eye gaze, head position, activation,” Collins says. “It’s rewarding to go through the journey of getting strong because you learn so much about your body, and so much about yourself,” Collins says.</p>
          <p class="bold">5 Reasons You Should Become a Powerlifter</p>
          <p>Strength Translates to Everything: “Strength is the most valuable investment you can make in your life,” Collins says. “No athlete ever said, ‘I lost this game because I was too strong.’ No one wants to make more trips to the car when they’re unloading groceries.”</p>
          <p>You’ll Become Mentally Tougher: Just as endurance training ultimately becomes a mental battle against your lungs, powerlifting demands steely psychological composure—particularly because training often entails pushing to failure, and then getting right back on the platform. “The barbell teaches you discipline, sacrifice, how to power through a weight you thought impossible, and how to navigate the darkness of hitting a weight that you’ve never done before,” Collins says.</p>
          <p>It’s Safe: Under the eye of a good coach and with the right prep, powerlifting’s injury rate is far below that of sports like soccer or basketball; it’s closer to tennis and volleyball, according to a widely cited 1994 study conducted in the U.K and published in the Journal of Strength and Conditioning Research. (Powerlifting has also been shown to decrease the risk of osteoporosis.)</p>
          <p>It Rewards Longevity: Basketball players are “old” by the time they’re 30, and football players and soccer players peak in their 20s. Powerlifters, on the other hand, are typically at their strongest at 35–40 years old, making powerlifting an accessible sport if, for some reason, you didn’t start it in your teens. Credit the phenomenon of “old-man strength,” the low injury risk, and the psychological veterancy.</p>
          <p>But You Should Know: As with any pursuit from bodybuilding to basketball, progressing in powerlifting becomes progressively more difficult, requiring more effort and dedication as time goes on. And, as with anything worth doing, it takes time and focus. “The road to 90% is relatively easy,” Collins says. “The road from 90 to 100 is hard as hell.” That, and “your friends seem to ask you for help a lot more often when they’re moving out of their apartments,” he says.</p>
          <p class="bold">What You'll Need to Start Powerlifting</p>
          <p>It sounds obvious, but choosing your gym is a key step. Sure, you can theoretically train for powerlifting in the average big-box gym. But if you’re serious about the sport, then training at a powerlifting-friendly gym—or at least one with a setup that allows you to take your time on the bench press and squat stations without getting dirty looks from the staff and other lifters—is ideal. (Check out USA Powerlifting’s gym search tool to find one near you.)</p>
          <p>You’ll also be able to watch and learn from knowledgeable powerlifters and coaches. If there aren’t any around, you can always inquire at your local CrossFit gym—some have separate powerlifting clubs that train independently of CrossFit classes.</p>
          <p>Otherwise, powerlifting doesn’t require too much gear of your own. Powerlifters often wear powerlifting-specific shoes (our recommendations here), which offer a solid, slightly raised heel to help lifters get into better position underneath the barbell while still engaging their posterior chain. For deadlifting, go with a pair of simple Chuck Taylor All-Stars or deadlifting slippers, since their thin rubber heels reduce a deadlifter’s range of motion and maximize the foot’s surface area with the floor.</p>
          <p>Another help: Chalk. “It aids your grip when deadlifting and benching, and improves the tightness of your squat,” Collins says.</p>
          <p>Many powerlifters invest in specialized equipment like knee sleeves and lifting belts, but you can probably hold off on those until you seriously commit to powerlifting.</p>
