        <p class="bold">Workout and Exercise</p>
        <p>In the 60s, most workouts were geared for three days a week. Upper body was done on one day and lower on the next. The gains came pretty good because there was enough rest time between the workouts and body parts. The sets and reps were 3 sets per exercise with 8 to 10 reps. This was very basic, but worked for most people, as it wasn’t overtraining.</p>
